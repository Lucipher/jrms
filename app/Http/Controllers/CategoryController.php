     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      try {
        $id = DB::table('categories')->count();
        $catadd = new Category;
        $catadd->id = $id+1;
        $catadd->category = $request->newcat;
        $catadd->save();
        return redirect()->back();
      }
      catch (\Exception $e)
      {
        return $e->getMessage();
      }
    }

